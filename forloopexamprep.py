#hyper simple for loops python

# Define a sequence (in this case, a list)
my_list = [1, 2, 3, 4, 5]

# Loop over each element in the sequence
for element in my_list:
    # Print each element
    print("Current element:", element)

# Print after the loop ends
print("Loop ended.")
